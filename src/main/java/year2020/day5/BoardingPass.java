package year2020.day5;

public class BoardingPass {

	private String binarySpacePartitioning;
	private int row;
	private int column;
	private int seatId;

	public String getBinarySpacePartitioning() {
		return binarySpacePartitioning;
	}

	public void setBinarySpacePartitioning(String binarySpacePartitioning) {
		this.binarySpacePartitioning = binarySpacePartitioning;
	}

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getColumn() {
		return column;
	}

	public void setColumn(int column) {
		this.column = column;
	}

	public int getSeatId() {
		return seatId;
	}

	public void setSeatId(int seatId) {
		this.seatId = seatId;
	}


}
